class Main {
    public static void main(String args[]) {
  
      // create char variables
      char a = '9';
      char b = '3';
  
      // convert char variables to int
      // by subtracting with char 0
      int num1 = a - '0';
      int num2 = b - '0';
  
      // print numeric value
      System.out.println(num1);    // 9
      System.out.println(num2);    // 3
    }
  }