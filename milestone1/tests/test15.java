class Main {
    public static void main(String args[]) {
  
      // create int variable
      int num1 = 36;
      int num2 = 99;
  
      // convert int to string
      // using valueOf()
      String str1 = String.valueOf(num1);
      String str2 = String.valueOf(num2);
  
      // print string variables
      System.out.println(str1);    // 36
      System.out.println(str2);    // 99
    }
  }